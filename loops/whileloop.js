// While loop:

// Simple Definition:
// A while loop runs as long as the condition is true.

// Technical Definition:
// A while loop is an entry-controlled loop that repeatedly executes a block while a boolean condition evaluates to true.

// Why it is Used, when number of iterations is unknown.

// Real-World Uses
// Waiting for user input
// Game loops
// Reading data continuously

// Syntax
// while(condition) {
//     // code
// }

// explaination:- 
let i = 1;
while(i <= 5) {
     
    console.log(i);
   i++;
}
